package com.model;

public class NewsCommentModel {

	private String news_comment_name = "";
	private String news_comment_comment = "";
	private String news_comment_time = "";
	
	public String getNews_comment_name() {
		return news_comment_name;
	}

	public void setNews_comment_name(String news_comment_name) {
		this.news_comment_name = news_comment_name;
	}

	public String getNews_comment_comment() {
		return news_comment_comment;
	}

	public void setNews_comment_comment(String news_comment_comment) {
		this.news_comment_comment = news_comment_comment;
	}

	public String getNews_comment_time() {
		return news_comment_time;
	}

	public void setNews_comment_time(String news_comment_time) {
		this.news_comment_time = news_comment_time;
	}

}
